var numbers=80;

switch(numbers){
    case 100:
        console.log('Grade is A+');
        break;
    case 80 :
        console.log('Grade is A+');
        break;
    case 70:
        console.log('Grade is A');
        break;
    case 60 :
        console.log('Grade is A-');
        break;
    case 50:
        console.log('Grade is B');
        break;
    case 40 :
        console.log('Grade is C');
        break;
    default:
        console.log('this is fail');
}



